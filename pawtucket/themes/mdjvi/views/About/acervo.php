			<h3>Acervo</h3>
			<p>As coleções da antiga Academia Imperial, depois Escola Nacional de Belas Artes e hoje EBA/UFRJ, foram formadas por obras de professores e alunos, provenientes de concursos para prêmios de viajem ou para vagas de professor, cópias realizadas nos museus europeus e todo o material didático das diversas disciplinas. Esse acervo extraordinário, acumulado desde a criação da Academia em 1816, foi desmembrado em 1937: grande parte dele deu origem ao Museu Nacional de Belas Artes, continuando na Escola, de modo geral, o acervo mais ligado ao ensino e o arquivo documental – origem do Museu D. João VI, criado em 1979.</p>

<p>A fundação do Museu não se restringe apenas à preservação do seu acervo, mas também cumpre outro papel primordial como museu universitário: por estar ligado a uma escola de arte – seria inadmissível pensarmos numa escola de arte sem obras de arte! Tem um compromisso fundamental com a pesquisa e a produção do conhecimento. Todos sabemos as fortunas fabulosas que as universidades americanas gastam para dotar os seus departamentos de arte de coleções que possam servir de referências para seus estudos, igualando-se em importância à biblioteca como apoio à formação do artista.</p>

<p>Assim, o acervo do Museu D. João VI serve às aulas de desenho e história da arte, é usado como laboratório nas disciplinas ligadas à restauração e contribui na formação dos alunos dos oito cursos de graduação da EBA, pois para todos os profissionais no campo da visualidade, é importante a compreensão tanto da tradição quanto da contemporaneidade.</p>

<p>Como museu universitário, atende a estudiosos de todo Brasil e mesmo do exterior – uma vez que seu acervo é importante para a compreensão da arte brasileira dos séculos XIX e CC. Além disso, tem servido de base para uma das principais linhas de investigação da Pós-graduação, com uma produção significativa de dissertações, teses e publicações sobre a história da instituição e a questão do ensino artístico.</p>

<p>O Museu D. João VI possui aproximadamente 3.653 peças museológicas e 6.221 documentos.</p>
